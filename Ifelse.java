public class Ifelse {
    public static void main(String[] args) {
        if (5 > 3) {
            System.out.println(1);
        }
        if (3 > 2) {
            System.out.println(2);
        }

        if (5 >= 4) {
            System.out.println(1);
        } else {
            System.out.println(2);
        }

        if (5 > 3)
            System.out.println(1);
        else
            System.out.println(2);
    }

}
